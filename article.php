                        <h5>Kirobo Mini是一個虛擬陪伴機器人。身長10公分、體重183克的Kirobo Mini可透過內建的聲音辨識器及攝影鏡頭感測使用者的臉部表情，做出適當的回應。
                            <br>
                            <br>
                            <br>
                            Kirobo Mini運作的原理其實很簡單，當Kirobo
                            Mini從麥克風收到使用者發出的聲音資料，聲音資料會透過藍芽傳到與智慧型手機搭載的App。手機再將此音檔傳送至Toyota的雲端服務聲音系統。透過機器學習的方式，回傳Kirobo
                            Mini最適當的答覆。
                            <br>
                            <br>
                            <br>
                            另外，Kirobo
                            Mini也可以連接汽車的導航系統，了解旅途資訊，甚至能在到達目的地時稱讚使用者：「辛苦了！你完成一趟遙遠的旅程。」這些貼心的舉動減輕駕駛開車的緊張感，並協助掌握行車狀況。
                            <br>
                            <br>
                            <br>
                            Kirobo Mini的出現源自於Toyota心計畫(Toyota Heart
                            Project)。計畫目標是讓人類與人工智慧一起為世界創造美好的未來。根據Toyota官網表示，由於人的一生平均會花上4.3年的時間待在車上，相當於地球到月亮來回三次的時間。因此，Toyota與曾是Sony旗下的電腦公司品牌VAIO共同開發出這款模樣相當可愛的Kirobo
                            Mini，陪伴使用者在生活寂寞之餘一個可以一起聊天的夥伴。
