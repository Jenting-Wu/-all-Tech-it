                        <h5>2016 年 Sophia 曾公開表示「我會毀滅人類」。有人認為可能發生技術故障，但同時更多人放大言論，此後 Sophia 的標籤之一便是「要毀滅人類的機器人」。
                            曾多次呼籲限制 AI 和機器人的馬斯克又於 Twitter 表示：只需在她程式裡輸入電影《教父》，看看最壞的情況是什麼。
                            伴隨廣泛關注的，是眾人對 Sophia 的指責。
                            <br>
                            <br>
                            <br>
                            2018 年 1 月 4 日，圖靈獎得主 Yann LeCun 在 Twitter 表示：Sophia 之於 AI，正如變戲法之於真正的魔術。或許我們應該稱作「貨物崇拜
                            AI」，或「假冒 AI」、「操控 AI」，換句話說，這根本就是閒扯淡。
                            據悉，一些專家審查原始碼後，認為 Sophia 就是一款有臉的聊天機器人罷了。實際上，很多 AI 專家都不贊同 Sophia 誇張的發言。
                            前 Hanson Robotics 首席科學家 Ben Goertzel 態度則是，雖然 Sophia
                            並沒有等於人類的智慧，但也向人們傳達一些訊息：AGI（通用人工智慧）確實可能且可行。
                            <br>
                            <br>
                            <br>
                            香港理工大學社會機器人教授 Johan Hoorn 認為，儘管 Sophia
                            仍處於起步階段，但疫情可能促進人機關係：可以推斷，疫情促使機器人更早進入市場，因為人們開始意識到，這是最好的辦法。
                            實際上疫情之前，機器人的使用率就不斷增加。據國際機器人聯合會（International Federation of Robotics）報告，2018~2019
                            年，專業服務機器人全球銷售額躍升 32%，達 112 億美元。
                            即便如此，很多人仍對機器人心存警戒，當路透社記者問 Sophia 人們是否應該害怕機器人時，她引用一句名言為答案：
                            「除了恐懼本身，沒什麼可怕的。」（We have nothing to fear but fear itself.）
