                        <h5>Hanson Robotics 最新願景是 2021 年底大規模量產機器人──包括 Sophia 等 4 款機器人將在上半年開始出貨。同時 Hanson Robotics
                            還將在今年推出名為 Grace 的新機器人，專門用於醫療保健業。
                            主因在疫情，如 Hanson Robotics 創始人兼首席執行長 David Hanson 所說：
                            新冠疫情將為機器人業帶來新機遇，世界需要越來越多自動化來保護人類的安全。
                            <br>
                            <br>
                            <br>
                            David Hanson 認為，機器人能做的不限於醫療保健領域，零售、航空等行業都能有番作為──Sophia
                            的獨特之處在於和人類非常相似，人們極度孤獨或被社會孤立時，將會非常有用。
                            路透社報導，記者參觀實驗室時，Sophia 還擔任解說員，並表示：像我這樣的社交機器人可以照顧病人、老人，我可以協助溝通、促進社交。
                            <br>
                            <br>
                            <br>
                            David Hanson 表示將銷售「數千台」機器人，但並未提供具體數字。截至 2018 年，Sophia 的架構包括腳本軟體、聊天系統和開源框架
                            OpenCog（一般推理用的人工智慧系統）。
                            技術方面，Sophia 有以下幾個特點：
                            語音辨識技術來自 Google 母公司 Alphabet，據說設計隨著時間推移會更智慧；語音合成能力、唱歌能力由 Cereproc 的文本─語音引擎提供。
                            軟體由 Hanson Robotics 設計，會分析對話並提取數據，以便改善應答。
                            眼睛部分結合電腦演算法鏡頭，因此能看見東西；不僅如此，看到人臉時還會保持眼神交流，可認得人。
                            皮膚由名叫 Frubber 的仿生皮膚材料製成，類似彈性橡膠，毛孔大小 4~40 毫微米（十億分之一公尺），和人類幾乎沒有差別。
                            <br>
                            <br>
                            <br>
                            Sophia 亮相後，世界各地媒體紛紛報導，並高調參與許多採訪，還登台過中國央視。
                            <br>
                            <br>
